export class UserMenuComponent {
  authService = inject(AuthService);

  getInitials(name: string): string {
    return name
      .split(' ')
      .map(part => part[0])
      .join('')
      .toUpperCase()
      .slice(0, 2);
  }

  signIn(provider: AuthProvider): void {
    this.authService.login(provider);
  }

  signOut(): void {
    this.authService.logout();
  }
}
